#!/bin/bash

# ==============================================================================
# プロジェクト設定
# ==============================================================================
TARGET="app.out"         # 生成する実行ファイル名
SRC_DIR="src"            # ソースコードのディレクトリ
INC_DIR="include"        # ヘッダーファイルのディレクトリ

# コンパイラとオプションの設定
CC="gcc"
CFLAGS="-Wall -Wextra -O2 -I$INC_DIR"

# ==============================================================================
# ビルド処理
# ==============================================================================
echo "=== ビルドを開始します ==="

# 1. srcディレクトリ内のすべての .c ファイルを取得
SRCS=$(find "$SRC_DIR" -name "*.c")

if [ -z "$SRCS" ]; then
    echo "[ERROR] ソースファイル（.c）が見つかりません。"
    exit 1
fi

echo "コンパイル対象:"
echo "$SRCS"
echo "--------------------------------------------------"

# 2. コンパイル実行
$CC $CFLAGS $SRCS -o "$TARGET"

# 3. 終了ステータスのチェック（商用開発の基本）
if [ $? -eq 0 ]; then
    echo "--------------------------------------------------"
    echo "[SUCCESS] ビルドが正常に完了しました！ -> ./$TARGET"
    echo "=================================================="
else
    echo "--------------------------------------------------"
    echo "[ERROR] ビルドに失敗しました。上記のエラーを確認してください。"
    echo "=================================================="
    exit 1
fi