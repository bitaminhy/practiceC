#include "calc.h"


/* 足し算を実施する関数 */
int add (int a,int b) {
  /* 計算した値を返す */

  return a + b ;
}

/* 引き算算を実施する関数 */
int dec (int a,int b) {
  /* 計算した値を返す */
  return a - b ;
}

/* 掛け算を実施する関数 */
int mul (int a,int b) {
  /* 計算した値を返す */
  return a * b;
}

/* 割り算を実施する関数 */
int divis (int a,int b) {
  /* 計算した値を返す */
  return a / b;
}

int rema (int a,int b) {
  return a % b;
}
